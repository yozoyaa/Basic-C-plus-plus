#include <iostream>

int main() {
    int Myint = 10;
    float MyFloat = 4.99f;
    char MyChar = 'a';
    bool MyBool = true;

    std::cout << "Integer : " << Myint << std::endl;
    std::cout << "Float : " << MyFloat << std::endl;
    std::cout << "Char : " << MyChar << std::endl;
    std::cout << "MyBool : " << MyBool << std::endl;

    return 0;
}